package com.yds.java8;

/**
 * 商品Service的Entity
 * @author 
 *
 */
public class ShohinServiceEntity {
	
	/** 商品Service code */
	private String shohinServiceCd;
	
	/** 商品Service名称 */
	private String shohinServiceName;

	/**
	 * @return the shohinServiceCd
	 */
	public String getShohinServiceCd() {
		return shohinServiceCd;
	}

	/**
	 * @param shohinServiceCd the shohinServiceCd to set
	 */
	public void setShohinServiceCd(String shohinServiceCd) {
		this.shohinServiceCd = shohinServiceCd;
	}

	/**
	 * @return the shohinServiceName
	 */
	public String getShohinServiceName() {
		return shohinServiceName;
	}

	/**
	 * @param shohinServiceName the shohinServiceName to set
	 */
	public void setShohinServiceName(String shohinServiceName) {
		this.shohinServiceName = shohinServiceName;
	}
	

}
